		<h2> Pais-em-Rede a Nível Nacional </h2>
		<p> 
			Pais em Rede tem o estatuto de “Organização Não Governamental para Pessoas com Deficiência” (ONGPD) Nacional. É constituída por uma rede de núcleos, e tem como objectivo promover a inclusão social das pessoas com deficiência e suas famílias, centrando-se na Cidadania Ativa.
			<br>
			<br>
			A pequena história de Pais-em-Rede formaliza-se a 5 de Novembro de 2008 a partir de um grupo de mães que queriam mudar a sociedade pela inclusão dos seus filhos com deficiência. Aprovado o PROJETO na 1ª assembleia geral em Lisboa em 14 de Maio de 2009, a Associação começa a funcionar com base na Direção Nacional e a descentralizar-se em diferentes núcleos, o 1º dos quais surge, em Setembro, no Porto. É-nos atribuído o estatuto de IPSS.
			<br>
			<br>
			Para mais informações visite-nos em <a id="link" href="http://paisemrede.pt/">paisemrede.pt</a> e no nosso <a id="link" href="https://www.facebook.com/PaisEmRedeAssociacao">Facebook</a>
		</p>

		<br>
		<hr >		

		<h2> Missão e Valores </h2>

		<h3> Missão </h3>
		<p>
			Congregar, num movimento de âmbito nacional, famílias das pessoas com todo o tipo de incapacidades, mobilizando toda a sociedade para a causa comum da sua inclusão.
		</p>

		<h3> Valores </h3>
		<p>
			Diálogo Persistente e Criativo com vista à INCLUSÃO
			<br>
			“Inclusão/Diálogo/ Criatividade/ Coerência/ Pragmatismo/ Flexibilidade/ Persistência/ Utopia / Colaboração”
		</p>

		<h3> Linhas de Ação </h3>
		<p>
			Para cumprir a sua MISSÃO, Pais-em-Rede dinamiza-se numa matriz com 3 grandes vectores:
			<br>
			<b> 1 - </b> Rede nacional de “famílias especiais”, profissionais e amigos, constituída em núcleos, de modo a promover uma real descentralização e, consequente interação in loco, respeitando as diversidades regionais.
			<br>
			<b> 2 - </b> Capacitação dos pais no processo de inclusão dos filhos com deficiência através de co-aprendizagens e formações que permitam aos pais um equilíbrio emocional e capacitação, facilitadores de um desempenho adequado e de um trabalho em equipa.
			<br>
			<b> 3 - </b> Construção e efetivação de respostas locais “alternativas” para a inclusão.
		</p>

